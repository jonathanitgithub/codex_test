function isAppointmentAvailable($date) {
    global $con;

    // Prepare SQL statement to check if the appointment date is already taken
    $sql = "SELECT * FROM appointments WHERE date = '$date'";

    // Execute SQL query
    $result = $con->query($sql);

    // If there are rows returned, the appointment date is already taken
    if ($result->num_rows > 0) {
        return false;
    } else {
        return true;
    }
}

